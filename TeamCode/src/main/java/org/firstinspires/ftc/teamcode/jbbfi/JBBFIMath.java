package org.firstinspires.ftc.teamcode.jbbfi;

public class JBBFIMath {
    /*
     * ADD
     */
    public int add(Integer one, Integer two){
        return one + two;
    }

    public double add(Double one, Double two){
        return one + two;
    }

    /*
     * SUB
     */
    public double sub(Double one, Double two){
        return one - two;
    }
    public int sub(Integer one, Integer two){
        return one - two;
    }

    /*
     * MULTIPLY
     */
    public double multiply(Double one, Double two){
        return one * two;
    }
    public int multiply(Integer one, Integer two){
        return one * two;
    }

    /*
     * DIVIDE
     */
    public double divide(Double one, Double two){
        return one / two;
    }
    public Integer divide(Integer one, Integer two){
        return one / two;
    }

}
